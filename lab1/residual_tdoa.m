function [h]=residual_tdoa(t,x,u,th)
    r1 = ((x(1)-th(1))^2 + (x(2)-th(2))^2)^(1/2);
    r2 = ((x(1)-th(3))^2 + (x(2)-th(4))^2)^(1/2);
    r3 = ((x(1)-th(5))^2 + (x(2)-th(6))^2)^(1/2);
    r4 = ((x(1)-th(7))^2 + (x(2)-th(8))^2)^(1/2);
    y1 = r2 - r1;
    y2 = r3 - r1;
    y3 = r4 - r1;
    h = [y1,y2,y3]';
end